import UIKit

class KeystrokeDetection:NSObject, UITextFieldDelegate {
    
    func textField(_ textField: UITextField, shouldChangeCharactersIn range: NSRange, replacementString string: String) -> Bool {
        switch string {
        case "a":
            <#code#>
        case "b":
            <#code#>
        case "c":
            <#code#>
        case "d":
            <#code#>
        default:
            <#code#>
        }
    }
    
}
